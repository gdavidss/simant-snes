/* sub_049966 (JSL from NMI) — walk the entity table, dispatch handlers,
 * then trim trailing empty slots.
 *
 * Verified body:
 *   PHB / DBR = $04
 *   X = $0600                              ; start of table
 *   loop: CPX $30 / BCC body / fall_through
 *   body:
 *     LDA $0000,x (8-bit type)
 *     BEQ skip                              ; type 0 = empty
 *     XBA / LDA #0 / XBA / ASL              ; A = type*2 (no bounds clamp!)
 *     TAY
 *     REP #$20 / LDA $9A30,y / STA $82      ; 16-bit handler addr
 *     SEP #$20
 *     JSR $DCD2 (= JMP ($0082))             ; call handler with X = entity ptr
 *   skip:
 *     X += 20
 *     BRA loop
 *   JSR $999A                                ; trim trailing empty slots
 *   PLB / RTL
 *
 * The trim helper at $04:999A walks BACKWARD from end of table to the
 * sentinel $05EC, finds the last non-zero type, and updates dp[$30] to
 * one-past-it so future iterations stop earlier.
 *
 * Note: there is NO bounds clamp on the type byte — the spawner must
 * keep type < 118 (the table size is 118 entries; values $76+ would
 * fetch garbage handlers). */
static void entity_table_compact_04999A(void)
{
    /* Faithful port of $04:999A: walk backward from the slot BEFORE the
     * current end (i.e. starting at X = walker's final X, which is the
     * smallest X >= dp[$30]). Stop at the sentinel $05EC (= $0600 - 20)
     * OR at the first non-empty slot. dp[$30] becomes that X + 20.
     *
     * Walker exits with X = some value >= dp[$30]. The walker increments
     * X by 20 each iteration starting from $0600, so X is always
     * $0600 + 20*k for some k >= 0. */
    uint16_t x = ENTITY_END;
    if (x < 0x0600) x = 0x0600;                  /* walker's first X */
    for (;;) {
        x -= sizeof(Entity);                      /* SBC #$0014 */
        if (x == 0x05EC) break;                   /* sentinel */
        if (wram[x] != 0) break;                  /* live entity */
    }
    ENTITY_END = x + sizeof(Entity);
}

static void entity_step_all_049966(void)
{
    /* ENTITY_END is the ABSOLUTE end-pointer (NOT an offset). Initial
     * value at boot is $0001, which makes the loop exit immediately
     * (since X starts at $0600 > $0001). After spawn_entity ($04:99C1),
     * dp[$30..$31] becomes "(slot_addr + 20)" so the walker reaches
     * exactly that slot. */
    for (unsigned x = 0x0600; x < ENTITY_END; x += sizeof(Entity)) {
        Entity *e = (Entity *)&wram[x];
        if (e->type == 0) continue;
        entity_handlers[e->type](e);
    }
    entity_table_compact_04999A();
}

