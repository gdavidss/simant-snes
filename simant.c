/*
 * SimAnt (SNES, Maxis / Imagineer / Tomcat Systems, 1993)
 *
 * Structural C reconstruction lifted from the 65816 disassembly. NOT a
 * runnable port — every ROM-side detail that hasn't been lifted yet is
 * a stub. What's modeled here:
 *
 *   - The complete boot path RESET → main_9340 → sub_BB8D → sub_896D.
 *   - The NMI handler and every per-frame helper it calls.
 *   - The cooperative task switcher built into the NMI tail.
 *   - The game-state pipeline (10 named states in $0B + dispatch table).
 *   - The entity system: 20-byte records, per-type init tables, dispatch
 *     through a 32-entry table at $04:9A30, per-state machines via byte 1.
 *   - The SPC700 sound-driver upload at $08:8006.
 *   - The VRAM/asset path: decompress to $7E:2000 → DMA into VRAM.
 *   - The 3-byte SRAM signature at $70:7FA0-A2 and bulk save at $70:0000.
 *   - The vblank-sync routine that handles SELECT (toggle view) and the
 *     joypad shadow reads used by sub_8101 (pause toggle).
 *
 * Manual-derived names come from the recovered ASCII strings (see
 * `strings.py`/`dig.py`) and the SimAnt instruction booklet vocabulary
 * (Yellow Ant, Black/Red colony, castes, scenarios, evaluation screens).
 *
 * File companions:
 *   disasm.txt — RESET, NMI, key subroutines (regenerated by disasm.py)
 *   dig.txt    — entity handler prologues, state handlers, HW scans,
 *                decompressor/save-game/SPC700-uploader call sites
 */

#include <stdint.h>

/* ========================================================================
 * MEMORY MODEL
 * ========================================================================
 *
 * SNES WRAM is 128 KB. Banks $7E and $7F map to it directly. Banks
 * $00-$3F at addresses $0000-$1FFF mirror $7E:0000-$7E:1FFF, which is
 * where most of the lifted code reads/writes. The direct-page register
 * is pinned at $0000 during NMI and during main_9340, so dp[i] is just
 * an alias for wram[i].
 *
 * Bank $70 in LoROM ($70:0000-$70:7FFF) is the 32 KB battery SRAM
 * declared in the cart header.
 */
static uint8_t  wram[0x20000];    /* $7E:0000 .. $7F:FFFF                  */
static uint8_t  sram[0x08000];    /* $70:0000 .. $70:7FFF                  */
#define dp wram                   /* direct page = wram[0..0xFF]           */

