/* ========================================================================
 * LZSS DECOMPRESSOR — $03:8467 (called via $02:8010 from $00:8D7E)
 *
 * See wiki/16-rendering-pipeline.md §8 for the LZSS format specification
 * verified across all 43 LZSS-compressed art assets. (The same codec is
 * re-used by the save system for the DOBBY/DURRY body — see
 * wiki/18-save-load.md §2.)
 *
 * Format decoded from the disassembly:
 *
 *   Header: 4 bytes
 *     [0..1]  uncompressed length (little-endian)
 *     [2..3]  skipped (probably reserved / unused)
 *
 *   Body: groups of 9 bytes max
 *     Each group starts with one "control byte". Bits are consumed MSB-first
 *     (the original uses ASL on a 16-bit register pre-seeded with $FF00 so
 *     the low byte counts off 8 shifts then the high $FF underflows).
 *       bit=0 -> emit one literal byte  (1 byte from stream)
 *       bit=1 -> back-reference          (2 bytes from stream):
 *                  bytes[0..1] together form a 16-bit field B
 *                    offset_back = (B >> 4) & 0x0FFF
 *                    length      = (B & 0x0F) + 3
 *                  Copy `length` bytes from out_pos-1-offset_back to out_pos.
 *
 *   Source pointer:  dp[$CF..$D1]      (24-bit; high byte = bank in dp[$D1])
 *   Output pointer:  dp[$D4..$D6]      (24-bit; dest set up by $00:8D7E to
 *                                       $7E:2000)
 *
 * Routine internals:
 *   sub_$03:8456     "read one byte from source and advance pointer"
 *   The decompressor uses DP=$0200 internally (TCD #$0200) so $C7-$DF refer
 *   to wram[$02C7..$02DF].
 * ======================================================================== */

/* Source byte reader. The original increments dp[$C7..$C9] and handles the
 * 16-bit bank rollover via ROR/INC at lines $843x. */
static uint8_t lz_read_byte(uint32_t *src24)
{
    uint8_t b = (*src24 < 0x100000) ? wram[*src24] : 0;
    (*src24)++;
    return b;
}

static void lz_decompress_03_8467(uint32_t src24, uint32_t dst24)
{
    uint16_t out_len = lz_read_byte(&src24);
    out_len |= lz_read_byte(&src24) << 8;
    (void)lz_read_byte(&src24);              /* skip 2 reserved bytes */
    (void)lz_read_byte(&src24);

    uint8_t *out = &wram[dst24 & 0x1FFFF];
    uint16_t out_pos = 0;
    while (out_pos < out_len) {
        uint8_t ctrl = lz_read_byte(&src24);
        for (unsigned bit = 0; bit < 8 && out_pos < out_len; ++bit) {
            if ((ctrl & 0x80) == 0) {
                /* literal */
                out[out_pos++] = lz_read_byte(&src24);
            } else {
                /* back-reference: 2 bytes, packed 12+4 */
                uint16_t b = lz_read_byte(&src24);
                b |= lz_read_byte(&src24) << 8;
                uint16_t offset = (b >> 4) & 0x0FFF;
                uint16_t length = (b & 0x0F) + 3;
                uint16_t src_pos = out_pos - 1 - offset;
                for (uint16_t i = 0; i < length && out_pos < out_len; ++i)
                    out[out_pos++] = out[src_pos++];
            }
            ctrl <<= 1;
        }
    }
}

