/* ========================================================================
 * RESET — $00:8009 (emulation-mode entry)
 * ======================================================================== */
static void main_9340(void);

void reset(void)
{
    /* SEI, CLD, CLC+XCE => native mode. DBR=0, D=0. */
    MDMAEN = 0;
    HDMAEN = 0;
    for (unsigned i = 0; i < 0x2000; ++i) wram[i] = 0;
    /* SP = $03FF. JMP $9340. */
    main_9340();
}

/* ========================================================================
 * main_9340 — task 0's body. Spawns task 1, calls boot inits, idles.
 * ======================================================================== */
static void boot_init_BB8D(void);
static void enable_nmi_896D(void);

/* See wiki/01-architecture.md "Game State Machine" section — 68-entry
 * dispatch at $00:9369, dp[$0B] is the state byte. Each handler is
 * run-once and INCs dp[$0B] to advance. */
static void game_state_dispatch_935C(void)
{
    /* Task 1 entry. Real handler table is at $9369 in ROM. */
    static void (* const dispatch[10])(void);
    for (;;) {
        unsigned s = dp[0x0B];
        if (s < 10 && dispatch[s])
            dispatch[s]();
    }
}

/* See wiki/01-architecture.md "main_9340" section */
static void main_9340(void)
{
    /* Asm @ $00:9340:
     *   LDY #$0002 / STY $02          ; TASK_LIMIT = 2 (one task already)
     *   LDA #$00 / LDX #$935C
     *   JSR $8113                      ; spawn_task(pc=$935C, a=$00)
     *   LDA #$01 / XBA / LDA #$00 / TCD   ; D := $0100  (NOT $0001)
     *   CLI                            ; unmask IRQs (defensive)
     *   JSR $BB8D                      ; boot_init
     *   JSR $896D                      ; enable_nmi
     *   BRA $935A                      ; spin forever; NMI scheduler yanks
     *                                  ;   us between this and task 1.
     *
     * The TCD sequence loads A=$0100 (A_hi=1, A_lo=0) then transfers to
     * D. Direct page = $0100 means all `LDA $xx` ZP loads in this task
     * read from $0100+xx, not $0000+xx. Important: the NMI handler
     * explicitly sets D=$0000 on entry, so dp[] semantics in the NMI
     * still target the bank-0 page. */
    TASK_LIMIT = 2;
    spawn_task(/*pc=*/0x935C, /*a=*/0x00);

    /* main task's direct page is $0100, not 0 — host-irrelevant since
     * the simulation uses absolute addressing for everything. */
    boot_init_BB8D();
    enable_nmi_896D();

    for (;;) ;
}

