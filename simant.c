/* External forward decl — defined in misc_helpers.c. */
extern void coop_yield_877D(void);

/* sub_985F — vblank-sync loop. Actual ROM entry is at $00:9859:
 *   JSR $877D / STZ $02E3 / [fall into $985F body]
 * Loop body at $985F:
 *   STZ NMITIMEN / JSR $E527 / NMITIMEN = $0A
 *   JSR $DEEE / JSR $DF79
 *   BCC $9877 (=break to A-button)  ; sub_DF79 returned carry-clear
 *   JSR $A3D6 / BRA $9859             ; retry path
 * After break:
 *   JSR $A354 (A button)
 *   if pause-locked: return
 *   else if SELECT held: STZ $26 / dp[$02B3]=1 / sub_A3BD / sub_8611 */
static void vsync_and_input_985F(void)
{
    coop_yield_877D();                /* $9859: JSR $877D before loop  */
    dp[0x02E3] = 0;
    for (;;) {
        NMITIMEN = 0;                 /* freeze NMI               */
        sub_E527();                   /* VRAM stream + tile-row queue  */
        NMITIMEN = dp[0x0A];          /* re-enable ($81)          */
        sub_DEEE();
        if (sub_DF79()) break;        /* "settled" => exit loop   */
        sub_A3D6();                   /* retry path               */
        coop_yield_877D();            /* asm BRA $9859 loops the yield */
        dp[0x02E3] = 0;
    }
    sub_A354();
    if (dp[0x0071]) return;           /* pause-locked => skip SELECT  */

    /* Note: reads $4219 (JOY1H) DIRECTLY, not the dp[$0161] shadow. */
    if (JOY1H & 0x20) {               /* SELECT held (bit 5)      */
        dp[0x0026]  = 0;
        dp[0x02B3]  = 1;               /* "view changed" flag      */
        switch_view_A3BD();
        after_view_change_8611();
    }
}

