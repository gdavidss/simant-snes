/* sub_C804 — VRAM update queue at $00:0C00.
 *   Batch format: [VMADDR][data...][$FFFF terminator].
 *   dp[$2C..$2D] = total bytes used; reset to 0 after flush. */
static void vram_queue_flush_C804(void)
{
    uint16_t end = *(uint16_t *)&dp[0x2C];
    if (end == 0) return;
    uint16_t *q = (uint16_t *)&wram[0x0C00];
    unsigned i = 0;
    while (i*2 < end) {
        VMADDL = q[i++];
        while (i*2 < end) {
            uint16_t w = q[i++];
            if (w == 0xFFFF) break;
            VMDATAL       = (uint8_t)w;
            MMIO8(0x2119) = w >> 8;
        }
    }
    *(uint16_t *)&dp[0x2C] = 0;
}

/* sub_8937 — CGRAM (palette) update-queue flusher. NOT an OAM index
 * reset (previous lift was wrong — verified at $00:8937 the body walks
 * the table at $0F20, NOT the OAM allocator).
 *
 * Format of the queue at WRAM $0F20:
 *   byte 0 = CGRAM index (written to CGADD $2121)
 *   byte 1 = low color byte (written to CGDATA $2122)
 *   byte 2 = high color byte (written to CGDATA $2122)
 * Total queue length (in bytes) is at dp[$2E..$2F] (16-bit). On exit
 * dp[$2E] is zeroed.
 *
 * Asm:
 *   LDX #$0000
 * loop:
 *   CPX $2E
 *   BNE $8944       ; if not done, process a triple
 *   LDY #$0000 / STY $2E / RTS
 * $8944:
 *   LDA $0F20,x / INX / STA $2121
 *   LDA $0F20,x / INX / STA $2122
 *   LDA $0F20,x / INX / STA $2122
 *   BRA loop
 */
static void cgram_queue_flush_8937(void)
{
    uint16_t queue_len = *(uint16_t *)&dp[0x2E];
    if (queue_len == 0) {
        *(uint16_t *)&dp[0x2E] = 0;             /* matches asm STY $2E */
        return;
    }
    for (uint16_t x = 0; x < queue_len; ) {
        MMIO8(0x2121) = wram[0x0F20 + x]; ++x;  /* CGADD */
        MMIO8(0x2122) = wram[0x0F20 + x]; ++x;  /* CGDATA low */
        MMIO8(0x2122) = wram[0x0F20 + x]; ++x;  /* CGDATA high */
    }
    *(uint16_t *)&dp[0x2E] = 0;
}
