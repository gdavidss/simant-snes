/* ========================================================================
 * COOPERATIVE TASK SCHEDULER
 * ========================================================================
 *
 * Task table arrays are interleaved through dp:
 *   dp[$0A + 2i]  saved SP for task i
 *   dp[$12 + 2i]  reload countdown
 *   dp[$1A + 2i]  current countdown
 *   dp[$22 + 2i]  enable flag (non-zero = scheduleable)
 * Up to 8 tasks. dp[$02] holds the in-use limit in bytes (2 * task_count).
 *
 * The NMI tail saves the interrupted task's SP at dp[$0A + 2*CUR_TASK],
 * advances to the next slot (mod limit), and for each:
 *   - if enabled, decrement its countdown
 *   - if countdown reaches 0, reload it, switch SP to that task, RTI
 *   - if countdown stays > 0, that task waits another frame; move on
 *
 * Effect: RTI pops PC and status from whatever stack we switched to, so
 * each task runs on its own stack page ($03xx, $04xx, ...). Tasks are
 * "preempted" only by NMI; between NMIs each task runs without
 * interruption.
 * ----------------------------------------------------------------------- */
#define CUR_TASK        dp[0x00]
#define TASK_LIMIT      dp[0x02]
#define TASK_SP(i)      (*(uint16_t *)&dp[0x0A + 2*(i)])
#define TASK_RELOAD(i)  (*(uint16_t *)&dp[0x12 + 2*(i)])
#define TASK_COUNT(i)   (*(uint16_t *)&dp[0x1A + 2*(i)])
#define TASK_ENABLE(i)  (*(uint16_t *)&dp[0x22 + 2*(i)])

/* spawn_task ($00:8113) — push a synthetic interrupt frame onto a fresh
 * stack page so the scheduler's eventual RTI lands at `pc` with the
 * standard register set restored. Verified asm:
 *
 *   STA $05 / STX $06          ; preserve A and X (PC)
 *   TSX / STX $08              ; preserve caller SP
 *   LDA $02 / LSR / CLC / ADC #$03  ; task_count + 3
 *   XBA / LDA #$FF / TCS       ; SP = ((task_count+3) << 8) | $FF
 *                              ; -> task 0 SP=$03FF, task 1 SP=$04FF, etc.
 *   LDA $05 / PHA              ; push 1 byte → PBR (= the A param)
 *   LDX $06 / PHX              ; push 2 bytes → PC (= the X param)
 *   PHP                        ; push 1 byte → P (status)
 *   PHX / PHX / PHX            ; push 3 × 2 bytes → initial X, Y, A
 *   PHB                        ; push 1 byte → initial B (data bank)
 *   LDA $02 / LSR / XBA / LDA #$00 / TAX / PHX
 *                              ; push 2 bytes → initial D (= task_count<<8)
 *   LDY $02 / TSX / STX $0A,y  ; save the new SP at dp[$0A+task_idx*2]
 *   LDA #$01
 *     STA $0012,y              ; reload = 1 (8-bit; high byte stays 0)
 *     STA $001A,y              ; count  = 1
 *     STA $0022,y              ; enable = 1
 *   INY / INY / STY $02        ; task_limit += 2
 *   LDX $08 / TXS / RTS        ; restore caller SP, return
 *
 * Stack frame for the scheduler's RTI sequence (bottom of stack → top):
 *   PBR (caller A) / PC (caller X) / P / X / Y / A / B / D
 *
 * The C model below ONLY updates the scheduler bookkeeping fields. The
 * synthetic stack frame is host-irrelevant — for a real port, replace
 * with setjmp/coroutine setup that resumes at `pc` with `a` in A. */
static void spawn_task(uint16_t pc, uint8_t a)
{
    unsigned slot = TASK_LIMIT >> 1;
    (void)pc; (void)a;
    TASK_RELOAD(slot) = 1;
    TASK_COUNT (slot) = 1;
    TASK_ENABLE(slot) = 1;
    TASK_LIMIT += 2;
}

