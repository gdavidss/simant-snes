/* sub_814F — VRAM streamer. Indexes a jump table at $815A by 2*dp[$88];
 * each entry sets up a DMA from a fixed source in WRAM to a fixed VRAM
 * destination. dp[$88] is set to 1..8 by sub_E527 (which keeps dp[$B0]
 * cycling 0..7 and writes dp[$B0]+1 → dp[$88]); index 0 routes to $81A4
 * (a bare RTS, no transfer this frame). The shared DMA-trigger tail at
 * $821A loads $4300=$01 (CPU→PPU word, fixed src), $4301=$18 (B-bus =
 * $2118 = VMDATAL), $4304=$00 (src bank), $4305=$0800 (len), then
 * MDMAEN=$01. */
static void vram_stream_step_814F(void)
{
    /* Index 0 = no-op (RTS at $81A4). Indices 1..8 each map to a 5-step
     * setup block at $81A5/$81B4/$81C3/$81D2/$81E1/$81F0/$81FF/$820E. */
    static const struct { uint16_t src; uint16_t vram_dst; } stream[9] = {
        { 0x0000, 0x0000 },               /* index 0 → no-op */
        { 0x3000, 0x0800 },               /* $81A5 */
        { 0x3800, 0x0C00 },               /* $81B4 */
        { 0x4000, 0x1000 },               /* $81C3 */
        { 0x4800, 0x1400 },               /* $81D2 */
        { 0x5000, 0x1800 },               /* $81E1 */
        { 0x5800, 0x1C00 },               /* $81F0 */
        { 0x6000, 0x2000 },               /* $81FF */
        { 0x6800, 0x2400 },               /* $820E (falls through into
                                             $821A shared tail) */
    };
    unsigned i = dp[0x88];
    if (i == 0 || i > 8) return;                /* index 0 = $81A4 RTS */
    DMA0_SRC   = stream[i].src;
    VMADDL     = stream[i].vram_dst;
    /* Shared DMA-trigger tail @ $821A:
     *   DMA0_PARAM=$01 (CPU→PPU, fixed src, word), DMA0_DEST=$18,
     *   DMA0_BANK=$00, DMA0_LEN=$0800, MDMAEN=$01. */
}

