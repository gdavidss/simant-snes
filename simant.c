/* ========================================================================
 * DIRECT-PAGE LAYOUT (what the lifted code actually uses)
 * ========================================================================
 *
 * $00..$04   wall clock (also: $00 = current cooperative task index)
 * $02        reused: at boot it's the task-count limit; during gameplay
 *            it's clock-seconds. Safe because the task count is set to 2
 *            at boot and only ever incremented by spawn_task (which runs
 *            before the clock starts).
 * $05..$09   spawn_task scratch (A=$05, X=$06.07, saved SP=$08.09)
 * $0A        $4200/NMITIMEN value ($81 = NMI + joypad auto-read)
 * $0A..$31   per-task scheduler tables (see SCHEDULER section below)
 * $0B        GAME STATE — index into the dispatch table at $9369
 * $0C..$11   "marker positions" — 6 bytes used by entity types 3/4/5
 *            to compute on-screen draw coordinates
 * $21        ?
 * $26        view mode (zeroed when SELECT toggles view)
 * $2A        pause flag (set by START; sub_8101)
 * $2C..$2D   VRAM update queue length, in bytes (queue at $0C00)
 * $2E..$2F   OAM index (sub_8937 zeros it)
 * $30..$31   entity-table end pointer (bytes used since $04:0600)
 * $32..$33   OAM allocator: next-free hi-priority slot ($0010 at boot)
 * $34..$35   OAM allocator: next-free lo-priority slot ($0110 at boot)
 * $37..$3E   draw helpers' scratch: $37/$38 = X, $39/$3A = Y, $3B/$3C =
 *            tile index, $3D = attribute
 * $44..$4F   misc game-state vars seeded in sub_BB8D
 * $46..$51   BG1/BG2/BG3 H+V scroll shadows (sub_884A pushes them)
 * $55        misc
 * $57..$58   "BG row changed?" guard for sub_8553
 * $5B        sub_85B2 state-machine guard (bails when >= 4)
 * $60..$61   joypad PREVIOUS-FRAME shadows (just-pressed edge detection)
 * $64        misc
 * $6C        screen-fade counter (used by $00:8616 fade loop)
 * $71        "lockout pause / SELECT" flag (set while a menu is open)
 * $73        misc
 * $81        ?
 * $82..$83   "indirect handler address" — set by sub_049966 before each
 *            entity-handler dispatch
 * $88        VRAM stream cursor (selects which of 8 fixed blocks to
 *            upload this frame; jump table at $815A)
 * $89        misc
 * $8C        misc
 * $E7..$E9   24-bit source pointer used by the SPC700 uploader
 *
 * Bigger pages:
 *   $0160/$0161  joypad CURRENT-FRAME shadows (JOY1L / JOY1H)
 *   $0200..$02FF reserved for menus / dialogs (some bytes seeded in
 *                sub_BB8D)
 *   $02B9/$02BA  16-bit pulse counter (incremented once every time the
 *                free-running tick wraps past 4 — roughly every 256
 *                frames)
 *   $02CF/$02D0  decompressor source pointer
 *   $02D1        decompressor "count" parameter (passed via A by $8D7E)
 *   $02D4/$02D5  decompressor dest address (set to $2000 by $8D7E)
 *   $02D6        decompressor dest bank   (set to $7E by $8D7E)
 *
 * Persistent SRAM scratch shadow at $7F:E710..$7F:E719 (purpose TBD).
 */

/* ========================================================================
 * GAME STATES — dp[$0B] indexes into the 10-entry table at $00:9369.
 * Each handler RUNS-ONCE: sets up its screen, then does INC dp[$0B] so the
 * NEXT frame the dispatcher picks up the following state.
 * ========================================================================
 *
 *   GS_FULL_GAME        $00:ACF3   silence APU, load FULL-game palette+map
 *   GS_SCENARIO_GAME    $00:AD5B   spawn 4 tasks via $877D, wait for count
 *                                  to hit 4, then fade out and advance
 *   GS_SAVED_GAME       $00:AC63   load SAVED-game menu palette+map
 *   GS_TUTORIAL         $00:ACE8   trivial: JSR $BA9E($04), $8616, INC
 *   GS_ANT_INFORMATION  $00:B155   silence APU, load encyclopedia screen
 *                                  (calls JSL $08:8003 to decompress)
 *   GS_MARRIAGE_FLIGHT  $00:B18C   the breeders' mating-flight screen
 *                                  (the manual ties this to pop>=100 +
 *                                  20 breeders — see p.19 "Expanding")
 *   GS_FULL_END         $00:B07B   FULL Game victory screen (3 asset loads)
 *   GS_SCENARIO_END     $00:B0FC   per-scenario victory screen; long JSL
 *                                  chain to $04:99BB / $04:99C1 to spawn
 *                                  the celebration entities
 *   GS_GAME_OVER        $00:B19F   force-blank, spawn type-$5E entity,
 *                                  re-enable NMI, advance
 *   GS_SOUND            $00:B1BF   Sound options screen ($BA9E + $BACA)
 * ----------------------------------------------------------------------- */
enum GameState {
    GS_FULL_GAME       = 0, GS_SCENARIO_GAME   = 1, GS_SAVED_GAME    = 2,
    GS_TUTORIAL        = 3, GS_ANT_INFORMATION = 4, GS_MARRIAGE_FLIGHT=5,
    GS_FULL_END        = 6, GS_SCENARIO_END    = 7, GS_GAME_OVER     = 8,
    GS_SOUND           = 9,
    /* Beyond 9 are runtime gameplay states. Save game lands at $1A
     * (see $00:959D: STA #$1A / $0B / RTS). */
    GS_AFTER_SAVE      = 0x1A,
};

