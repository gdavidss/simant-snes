/* ========================================================================
 * CURSOR ENTITY (type 01) — $04:9D9D
 *
 * Polled every NMI via the entity walker. The cursor "entity" is really
 * just an input pickup that decides whether the player pressed A or a
 * B/Y combination — then dispatches to the shared action handler at
 * $9DB9. dp[$60..$61] are the "freshly pressed" joypad edge bits (not
 * the raw $0160/$0161, which are current-frame).
 *
 * Verified disassembly @ $04:9D9D:
 *   LDA $0071
 *   BNE $9DB1        ; menu-open path — DOES NOT EARLY-RETURN; jumps to
 *                    ;   the mouse-edge fallback at $9DB1 which still
 *                    ;   accepts left/right clicks
 *   LDA $60 / AND #$80 / BNE $9DB9     ; A button just pressed
 *   LDA $61 / AND #$90 / BNE $9DB9     ; B or Y just pressed
 *   RTS
 *
 * $9DB1 (menu-locked fallback):
 *   LDA $007D / AND #$03 / BNE $9DB9   ; mouse left/right button edges
 *   RTS
 *
 * Both paths converge on $9DB9 (the action handler).
 * ======================================================================== */
static void cursor_action_A_9DB9(void);    /* shared action target */
static void cursor_action_B_9DB1(void);    /* menu-locked mouse fallback */

static void cursor_handler_04_9D9D(Entity *self)
{
    (void)self;
    if (dp[0x0071]) {                    /* menu open -> mouse fallback   */
        cursor_action_B_9DB1();
        return;
    }
    if (dp[0x60] & 0x80) {                /* bit 7 of $60 = A button edge  */
        cursor_action_A_9DB9();
        return;
    }
    if (dp[0x61] & 0x90) {                /* bits 7+4: B or Y edges        */
        cursor_action_A_9DB9();
        return;
    }
    /* nothing pressed this frame */
}
/* $04:9DB1 — menu-locked fallback. Accepts mouse buttons via dp[$007D]
 * (the just-pressed edge mask from the mouse BIOS). Bits 0+1 of $007D
 * mirror the mouse's left+right buttons after the BIOS re-packs them. */
