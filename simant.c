/* See wiki/01-architecture.md "NMI handler" section — per-frame pipeline,
 * scheduler SP-swap, wall clock at dp[$00..$04].
 *
 * See wiki/16-rendering-pipeline.md for the full NMI rendering pipeline:
 * shadow-OAM DMA, VRAM streamer (sub_814F + jump table $815A), VRAM
 * update queue ($00:0C00 / sub_C804), CGRAM trickle flush, BG scroll
 * push, shadow-OAM clear (sprite parking at Y=$E0), and the per-view
 * tile/palette tables at $01:996F / $9A5F. */
void nmi(void)
{
    (void)RDNMI;                              /* acknowledge */

    /* Shadow OAM DMA: 0x220 bytes from $00:0D00 -> $2104. */
    DMA0_PARAM = 0x00;
    DMA0_DEST  = 0x04;                        /* $2104 = OAMDATA */
    DMA0_SRC   = 0x0D00;
    DMA0_BANK  = 0x00;
    DMA0_LEN   = 0x0220;
    MDMAEN     = 0x01;

    vram_stream_step_814F();
    if ((CUR_TASK & 1) == 0) per_frame_even_8553();
    else                     per_frame_odd_85B2();

    vram_queue_flush_C804();
    oam_index_reset_8937();
    bg_scroll_push_884A();
    shadow_oam_clear_88A5();
    entity_step_all_049966();
    pause_toggle_on_start_8101();

    /* Wall clock at dp[$00..$04]. dp[$00] is also the cooperative-task ID. */
    dp[0x00]++;
    if (++dp[0x01] >= 60) {
        dp[0x01] = 0;
        if (++dp[0x02] >= 60) {
            dp[0x02] = 0;
            if (++dp[0x03] >= 60) {
                dp[0x03] = 0;
                dp[0x04]++;
            }
        }
    }

    /* Once every 256 frames (when CUR_TASK rolls past 4): bump the
     * long-tick counter at dp[$02B9..$02BA]. */
    if (dp[0x00] == 4) (*(uint16_t *)&wram[0x02B9])++;

    scheduler_switch_and_rti();               /* SP-swap + RTI */
}

