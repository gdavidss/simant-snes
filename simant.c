/* ========================================================================
 * ENTITY SYSTEM — 32 types, 20-byte records, per-type per-state machines.
 * ========================================================================
 *
 * Layout decoded from spawn helper $04:99C1 and the per-handler prologues
 * (most handlers do `TXY / LDA $0001,x / ASL / TAX / JMP (table)` — i.e.
 * they switch on byte +1).
 *
 *   +0   type            (1 byte; index into entity_handlers[])
 *   +1   state           (1 byte; per-type state-machine index)
 *   +2-3 x position      (2 bytes, 16-bit)
 *   +4-5 y position      (2 bytes)
 *   +6   flag            (1 byte; spawn zeroes it)
 *   +7-B scratch         (5 bytes; per-handler use)
 *   +C-D init constant   (2 bytes; copied at spawn from table $01:EF59)
 *   +E   zero            (spawn-time)
 *   +F   init attribute  (1 byte; copied from $01:F043 — looks like an
 *                         OAM-style priority+palette byte for sprite types)
 *   +10  zero            (spawn-time)
 *   +11-13 trailing scratch
 *
 * Handler dispatch:
 *   sub_049966 in bank $04 walks the table at $04:0600 from X=$0600 until
 *   X >= dp[$30]. For each entity whose type != 0, it tail-calls the
 *   handler in the 32-entry table at $04:9A30. Empty slots have type=0;
 *   no special "dead" marker needed.
 *
 * Aliases:
 *   type 19 == 18 ($04:A533) — likely "queen" variants share AI
 *   type 21 == 16 ($04:A356)
 *   type 22 == 10 ($04:9E9C) — likely B/R variant share
 *
 * Heuristic role guesses (LIFT EACH HANDLER TO CONFIRM):
 *   type  0:  empty slot                        — handler is `RTS`
 *   type  1:  cursor / input pickup             — handler reads dp[$60/$61]
 *                                                 (JOY1 prev) for A & B
 *   type  2:  some menu/screen polled element   — checks dp[$004E] sign
 *   type  3:  selection-rectangle marker        — draws at dp[$0C/$0D]
 *   type  4:  marker variant                    — draws at dp[$0E/$0F]
 *   type  5:  marker variant                    — draws at dp[$10/$11]
 *   type  6:  countdown-driven object           — checks $02B2 vs $024A
 *   type  7:  static UI prop at dp[$0E/$0F]
 *   type  8:  static UI prop at dp[$10/$11]
 *   types 9-22: creatures (ants / spider / ant lion / caterpillar) —
 *               all use the per-state JMP table pattern
 *   type 18/19: Queen Ant (Black + Red variants alias to same handler)
 *   type 26:    "recruit ring" or particle generator (spawns sprites in a
 *               grid stepping +$40 in X and Y; see $04:AB0B)
 *   type 29:    pop-up dialog gate (checks dp[$02A7])
 *   type 31:    menu gate (checks dp[$0200])
 *
 * The MAP from manual characters to type IDs needs lifting the handler
 * bodies, not just prologues — what each one DOES in state 0 vs state 1
 * etc. is what distinguishes Worker from Soldier from Spider.
 * ----------------------------------------------------------------------- */
typedef struct __attribute__((packed)) Entity {
    uint8_t  type;         /* +0   */
    uint8_t  state;        /* +1   per-type state-machine index */
    uint16_t x;            /* +2-3 */
    uint16_t y;            /* +4-5 */
    uint8_t  flag;         /* +6   */
    uint8_t  scratch[5];   /* +7-B */
    uint16_t init_word;    /* +C-D  from table $01:EF59[type] */
    uint8_t  pad_e;        /* +E   */
    uint8_t  init_attr;    /* +F   from table $01:F043[type] */
    uint8_t  pad_10;       /* +10  */
    uint8_t  tail[3];      /* +11-13 */
} Entity;
_Static_assert(sizeof(Entity) == 20, "entity record is 20 bytes");

